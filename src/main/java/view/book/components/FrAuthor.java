    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        view.components.DlgTextInput edtAuthor = new view.components.DlgTextInput(this, true, "Nome do autor");
        edtAuthor.setVisible(true);
        view.components.DlgTextInput edtHomeTown = new view.components.DlgTextInput(this, true, "Cidade natal do autor");
        edtHomeTown.setVisible(true);        
        
        lblAlert.setText("");
        listModel.addElement(
                new model.entity.Author(edtAuthor.getValue(), edtHomeTown.getValue())
        );
        lstAuthor.setModel(listModel);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (lstAuthor.getSelectedIndex() != -1) {
            listModel.remove(lstAuthor.getSelectedIndex());
            lblAlert.setText("Removido com sucesso!");
        } else {
            lblAlert.setText("Selecione um item para remover!");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

