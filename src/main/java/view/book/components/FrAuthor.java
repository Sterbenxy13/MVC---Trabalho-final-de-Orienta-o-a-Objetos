    public java.util.List<model.entity.Author> getAuthorValues() {
        java.util.List<model.entity.Author> result = new java.util.ArrayList<>();
        for (int i = 0; i < this.listModel.size(); i ++) {
            result.add(
                    listModel.getElementAt(i)
            );
        }
        return result;
    }

