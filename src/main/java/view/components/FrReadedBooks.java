    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        lblAlert.setText("");
        
        controller.BookManager bookManager
                = new controller.BookManager();
        
        view.components.FrSelect edtBook
                = new view.components.FrSelect(this, true, bookManager);
        edtBook.setVisible(true);
        
        if (!edtBook.wasSelected()) {
            return;
        }
        
        view.components.DlgTextInput edtGrade
                = new view.components.DlgTextInput(this, true, "Insira sua nota de 0 a 5 para o livro");
        edtGrade.setVisible(true);
        
        int grade;
        while (true) {
            try {
                grade = Integer.parseInt(edtGrade.getValue());
                if (grade < 0 || grade > 5) {
                    new view.components.DlgAlert(this, true, "A nota deve ser entre 0 e 5!");
                    edtGrade.setVisible(true);
                    continue;
                }
                break;
            } catch (Exception e) {
                new view.components.DlgAlert(this, true, "A nota deve ser um n√∫mero!");
                edtGrade.setVisible(true);
            }
        }
        
        String tempData = bookManager.getJsonData(edtBook.getEdtIndexValue());
        model.entity.Book temp = model.persistence.JSONService.fromJson(tempData, model.entity.Book.class).get(0);
        bookManager.addGrade(edtBook.getEdtIndexValue(), grade);
        this.manager.addReadedBook(this.userIndex, temp, grade);
        listModel.addElement(temp);
        lstBooks.setModel(listModel);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (lstBooks.getSelectedIndex() != -1) {
            listModel.remove(lstBooks.getSelectedIndex());
            lblAlert.setText("Removido com sucesso!");
        } else {
            lblAlert.setText("Selecione um item para remover!");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

