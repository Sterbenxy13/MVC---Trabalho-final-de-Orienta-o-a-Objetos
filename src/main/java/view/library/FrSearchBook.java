    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (this.edtSearchBy.getSelectedIndex() == 0) {
            this.books = this.manager.searchBookByTitle(this.edtSearchValue.getText());
        }
        if (this.edtSearchBy.getSelectedIndex() == 1) {
            this.books = this.manager.searchBookByGenre(this.edtSearchValue.getText());
        }
        if (this.edtSearchBy.getSelectedIndex() == 2) {
            this.books = this.manager.searchBookByAuthor(this.edtSearchValue.getText());
        }
        
        this.listModel.removeAllElements();
        for (model.entity.Book b: books) {
            listModel.addElement(b);
        }        
        this.lstBooks.setModel(this.listModel);
        
    }//GEN-LAST:event_btnSearchActionPerformed

