    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        this.lblAlert.setText("");
        if (this.lstBooks.getSelectedIndex() == -1) {
            this.lblAlert.setText("Selecione um livro para adicionar!");
            return;
        }
        
        view.components.DlgTextInput edtGrade
                = new view.components.DlgTextInput(this, true, "Insira sua nota de 0 a 5 para o livro");
        edtGrade.setVisible(true);
        
        int grade;
        while (true) {
            try {
                grade = Integer.parseInt(edtGrade.getValue());
                if (grade < 0 || grade > 5) {
                    new view.components.DlgAlert(this, true, "A nota deve ser entre 0 e 5!");
                    edtGrade.setVisible(true);
                    continue;
                }
                break;
            } catch (Exception e) {
                new view.components.DlgAlert(this, true, "A nota deve ser um número!");
                edtGrade.setVisible(true);
            }
        }
        this.bookManager.addGrade(lstBooks.getSelectedIndex(), grade);
        this.userManager.addReadedBook(this.userIndex, lstBooks.getSelectedValue(), grade);
        this.listModel.remove(lstBooks.getSelectedIndex());
        this.lstBooks.setModel(listModel);     
    }//GEN-LAST:event_btnAddActionPerformed

